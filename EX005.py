n = int(input("Digite um número: "))
ant = n -1
suc = n +1
dobro = n *2
metade = n /2
raiz = n ** (1/2)
print("O seu antecessor é", ant)
print("O seu sucessor é", suc)
print("O dobro de {} é {}".format(n, dobro))
print("A metade de {} é {}".format(n, metade))
print("A raiz quadrada de {} é {:.2f}".format(n, raiz)) # Duas casa decimais flutuantes





